def create_nerf(trainer, model):
    """Instantiate NeRF's MLP model.
    """
    embed_fn, input_ch = get_embedder(trainer.multires, trainer.i_embed)

    input_ch_views = 0
    embeddirs_fn = None
    if trainer.use_viewdirs:
        embeddirs_fn, input_ch_views = get_embedder(trainer.multires_views, trainer.i_embed)
    output_ch = 5 if trainer.N_importance > 0 else 4
    skips = [4]
    model_nerf = model(D=trainer.netdepth, W=trainer.netwidth,
                 input_ch=input_ch, output_ch=output_ch, skips=skips,
                 input_ch_views=input_ch_views, use_viewdirs=trainer.use_viewdirs).to(device)
    grad_vars = list(model_nerf.parameters())

    model_fine = None
    if trainer.N_importance > 0:
        model_fine = model(D=trainer.netdepth_fine, W=trainer.netwidth_fine,
                          input_ch=input_ch, output_ch=output_ch, skips=skips,
                          input_ch_views=input_ch_views, use_viewdirs=trainer.use_viewdirs).to(device)
        grad_vars += list(model_fine.parameters())

    network_query_fn = lambda inputs, viewdirs, network_fn : trainer.run_network(
        inputs=inputs,
        viewdirs=viewdirs,
        fn=network_fn,
        embed_fn=embed_fn,
        embeddirs_fn=embeddirs_fn,
    )

    # Create optimizer
    optimizer = torch.optim.Adam(params=grad_vars, lr=trainer.lrate, betas=(0.9, 0.999))

    start = 0
    basedir = trainer.basedir
    expname = trainer.expname

    ##########################

    # Load checkpoints
    if trainer.ft_path is not None and trainer.ft_path!='None':
        ckpts = [trainer.ft_path]
    else:
        ckpts = [os.path.join(basedir, expname, f) for f in sorted(os.listdir(os.path.join(basedir, expname))) if 'tar' in f]

    print('Found ckpts', ckpts)
    if len(ckpts) > 0 and not trainer.no_reload:
        ckpt_path = ckpts[-1]
        print('Reloading from', ckpt_path)
        ckpt = torch.load(ckpt_path)

        start = ckpt['global_step']
        optimizer.load_state_dict(ckpt['optimizer_state_dict'])

        # Load model
        model_nerf.load_state_dict(ckpt['network_fn_state_dict'])
        if model_fine is not None:
            model_fine.load_state_dict(ckpt['network_fine_state_dict'])

    ##########################

    render_kwargs_train = {
        'network_query_fn': network_query_fn,
        'perturb': trainer.perturb,
        'N_importance': trainer.N_importance,
        'network_fine': model_fine,
        'N_samples': trainer.N_samples,
        'network_fn': model_nerf,
        'use_viewdirs': trainer.use_viewdirs,
        'white_bkgd': trainer.white_bkgd,
        'raw_noise_std': trainer.raw_noise_std,
        'trainer': trainer
    }

    # NDC only good for LLFF-style forward facing data
    if trainer.dataset_type != 'llff' or trainer.no_ndc:
        print('Not ndc!')
        render_kwargs_train['ndc'] = False
        render_kwargs_train['lindisp'] = trainer.lindisp

    render_kwargs_test = {k : render_kwargs_train[k] for k in render_kwargs_train}
    render_kwargs_test['perturb'] = False
    render_kwargs_test['raw_noise_std'] = 0.

    return render_kwargs_train, render_kwargs_test, start, grad_vars, optimizer


def render_rays(
    ray_batch,
    network_fn,
    network_query_fn,
    N_samples: int,
    trainer,
    retraw=False,
    lindisp=False,
    perturb=0.,
    N_importance=0,
    network_fine=None,
    white_bkgd=False,
    raw_noise_std=0.,
    verbose=False,
    pytest=False,
    **kwargs
):
    """Volumetric rendering.
    Args:
      ray_batch: array of shape [batch_size, ...]. All information necessary
        for sampling along a ray, including: ray origin, ray direction, min
        dist, max dist, and unit-magnitude viewing direction.
      network_fn: function. Model for predicting RGB and density at each point
        in space.
      network_query_fn: function used for passing queries to network_fn.
      N_samples: int. Number of different times to sample along each ray.
      retraw: bool. If True, include model's raw, unprocessed predictions.
      lindisp: bool. If True, sample linearly in inverse depth rather than in depth.
      perturb: float, 0 or 1. If non-zero, each ray is sampled at stratified
        random points in time.
      N_importance: int. Number of additional times to sample along each ray.
        These samples are only passed to network_fine.
      network_fine: "fine" network with same spec as network_fn.
      white_bkgd: bool. If True, assume a white background.
      raw_noise_std: ...
      verbose: bool. If True, print more debugging info.
    Returns:
      rgb_map: [num_rays, 3]. Estimated RGB color of a ray. Comes from fine model.
      disp_map: [num_rays]. Disparity map. 1 / depth.
      acc_map: [num_rays]. Accumulated opacity along each ray. Comes from fine model.
      raw: [num_rays, num_samples, 4]. Raw predictions from model.
      rgb0: See rgb_map. Output for coarse model.
      disp0: See disp_map. Output for coarse model.
      acc0: See acc_map. Output for coarse model.
      z_std: [num_rays]. Standard deviation of distances along ray for each
        sample.
    """
    N_rays = ray_batch.shape[0]
    rays_o, rays_d = ray_batch[:,0:3], ray_batch[:,3:6] # [N_rays, 3] each

    viewdirs = ray_batch[:,-3:] if ray_batch.shape[-1] > 8 else None
    bounds = torch.reshape(ray_batch[...,6:8], [-1,1,2])
    near, far = bounds[...,0], bounds[...,1] # [-1,1]

    rgb_map, disp_map, acc_map, weights, depth_map, z_vals, _weights, raw = trainer.sample_main_points(
        near=near,
        far=far,
        perturb=perturb,
        N_rays=N_rays,
        N_samples=N_samples,
        viewdirs=viewdirs,
        network_fn=network_fn,
        network_query_fn=network_query_fn,
        rays_o=rays_o,
        rays_d=rays_d,
        raw_noise_std=raw_noise_std,
        white_bkgd=white_bkgd,
        pytest=pytest,
        lindisp=lindisp,
        **kwargs
    )

    rgb_map_0, disp_map_0, acc_map_0, rgb_map, disp_map, acc_map, raw_0, z_samples = trainer.sample_points(
        z_vals=z_vals,
        weights=_weights,
        perturb=perturb,
        pytest=pytest,
        rays_o=rays_o,
        rays_d=rays_d,
        rgb_map=rgb_map,
        disp_map=disp_map,
        acc_map=acc_map,
        network_fn=network_fn,
        network_fine=network_fine,
        network_query_fn=network_query_fn,
        viewdirs=viewdirs,
        raw_noise_std=raw_noise_std,
        white_bkgd=white_bkgd,
        **kwargs
    )

    ret = {'rgb_map' : rgb_map, 'disp_map' : disp_map, 'acc_map' : acc_map}
    if retraw:
        if raw_0 is None:
            ret['raw'] = raw
        else:
            ret['raw'] = raw_0
    if N_importance > 0:
        ret['rgb0'] = rgb_map_0
        ret['disp0'] = disp_map_0
        ret['acc0'] = acc_map_0
        ret['z_std'] = torch.std(z_samples, dim=-1, unbiased=False)  # [N_rays]

    for k in ret:
        if (torch.isnan(ret[k]).any() or torch.isinf(ret[k]).any()) and DEBUG:
            print(f"! [Numerical Error] {k} contains nan or inf.")

    return ret


